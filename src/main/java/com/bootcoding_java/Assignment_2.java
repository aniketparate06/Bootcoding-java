package com.bootcoding_java;

public class Assignment_2 {
    public static void main(String[] args) {
        float width= 5.6f;
        float height =8.5f;

        //Area of rectangle
        float areaOfRectangle=width*height;
        System.out.println("area of rectangle is "+width +" * "+height+" = "+areaOfRectangle);

        //Perimeter of rectangle
        float perimeterOfRectangle=2*(width+height);
        System.out.println("perimeter of rectangle is "+width +" * "+height+" = "+perimeterOfRectangle);
    }

}
